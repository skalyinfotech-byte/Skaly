<script>
    // Helper: Replace letters with random gibberish
    function toGibberish(text) {
      return text.replace(/[a-zA-Z]/g, () =>
        String.fromCharCode(97 + Math.floor(Math.random() * 26))
      );
    }

    // Convert all text nodes in the document
    function convertTextNodesToGibberish(node) {
      if (node.nodeType === 3) { // Text node
        node.nodeValue = toGibberish(node.nodeValue);
      } else {
        node.childNodes.forEach(convertTextNodesToGibberish);
      }
    }

    // Run after page load
    document.addEventListener("DOMContentLoaded", () => {
      convertTextNodesToGibberish(document.body);
    });
  </script>

